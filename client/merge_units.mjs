function getMajorUnit(subUnit) {
  if (!subUnit) return '기타/미분류';
  
  for (const [key, value] of Object.entries(UNIT_MAP)) {
    if (subUnit.includes(key)) return value;
  }
  
  // 어떠한 키워드도 안 맞으면... 적절한 추측
  if (subUnit.match(/전계|전위|콘덴서|가우스/)) return '[이론] 정전계와 콘덴서';
  if (subUnit.match(/자계|자기|자석|인덕턴스/)) return '[이론] 자기장과 인덕턴스';
  if (subUnit.match(/교류|임피던스|삼상|위상/)) return '[이론] 정현파 교류와 RLC 회로';
  if (subUnit.match(/발전기|전동기|전기자/)) {
    if (subUnit.includes('직류')) return '[기기] 직류 발전기와 전동기';
    if (subUnit.includes('동기')) return '[기기] 동기 발전기와 전동기';
    if (subUnit.includes('유도')) return '[기기] 유도 발전기와 전동기';
    return '[기기] 유도 발전기와 전동기'; // 기본값
  }
  if (subUnit.match(/공사|배선|관|덕트/)) return '[설비] 옥내 배선 및 덕트 공사';
  if (subUnit.match(/전선|케이블|재료|접속/)) return '[설비] 전선 및 배선 재료/공구';
  
  return '[설비] 기타 및 안전 수칙'; // 보수적인 대응
}

const files = fs.readdirSync(DATA_DIR).filter(f => f.endsWith('.json'));

files.forEach(file => {
  const filePath = path.join(DATA_DIR, file);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));
  
  let modified = false;
  if (data.questions) {
    data.questions.forEach(q => {
      const oldUnit = q.sub_unit;
      const newUnit = getMajorUnit(oldUnit);
      if (oldUnit !== newUnit) {
        q.sub_unit = newUnit;
        modified = true;
      }
    });
  }
  
  if (modified) {
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf-8');
    console.log(`Updated: ${file}`);
  }
});

console.log('All units have been merged into major categories!');
